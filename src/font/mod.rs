pub struct Character<'a> {
    pub bitmap: &'a [u8; BITMAP_HEIGHT],
}

impl Character<'_> {
    pub fn new<'a>(char_bitmap: &'a [u8;16]) -> Character<'a> {
        return Character {
            bitmap: char_bitmap,
        };
    }
}

